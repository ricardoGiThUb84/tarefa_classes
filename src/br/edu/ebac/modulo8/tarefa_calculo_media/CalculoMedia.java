package br.edu.ebac.modulo8.tarefa_calculo_media;

public class CalculoMedia {

    public static void main(String[] args) {

        double nota1 = 8.7;
        double nota2 = 9.5;
        double nota3 = 7.0;

        double media;

        media = nota1 + nota2 + nota3 / 3;

        System.out.println("A média das notas é: "+ media);

    }
}
